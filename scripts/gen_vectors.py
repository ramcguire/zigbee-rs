import zigpy.types as t

ZIGPY_VERSION = importlib.metadata.version("zigpy")

HEADER = f"""\
// Code generated by scripts/gen_vectors.py using zigpy {ZIGPY_VERSION}. DO NOT EDIT.
// Run `cargo xtask generate-vectors` to refresh.
"""

def fmt_bytes(b: bytes) -> str:
    return "&[" + ", ".join(f"0x{byte:02X}" for byte in b) + "]"


def fmt_roundtrip_scalar(pairs: list[tuple[bytes, str]], rust_type: str) -> str:
    lines = [f"    ({fmt_bytes(wire)}, {val}{rust_type})," for wire, val in pairs]
    return (
        f"pub static ROUNDTRIP: &[(&[u8], {rust_type})] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )


def fmt_roundtrip_bool(pairs: list[tuple[bytes, str]]) -> str:
    lines = [f"    ({fmt_bytes(wire)}, {val})," for wire, val in pairs]
    return (
        "pub static ROUNDTRIP: &[(&[u8], bool)] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )


def fmt_bytes_list(name: str, items: list[bytes]) -> str:
    lines = [f"    {fmt_bytes(b)}," for b in items]
    return f"pub static {name}: &[&[u8]] = &[\n" + "\n".join(lines) + "\n];\n"


def fmt_roundtrip_bits(pairs: list[tuple[bytes, str]], rust_type: str, inner: str) -> str:
    """For bitmaps: store as (wire, u64 raw value) since the Rust type is a newtype."""
    lines = [f"    ({fmt_bytes(wire)}, {val}{inner})," for wire, val in pairs]
    return (
        f"pub static ROUNDTRIP_RAW: &[(&[u8], {inner})] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )


def assemble(*sections: str) -> str:
    return HEADER + "\n" + "\n".join(s for s in sections if s)


def gen_uint(zigpy_type, rust_type: str, bits: int) -> str:
    max_valid = (1 << bits) - 2  # one below null sentinel
    null_val = (1 << bits) - 1

    values = [0, 1, max_valid // 2 + 1, max_valid]
    roundtrip = [
        (zigpy_type(v).serialize(), str(v))
        for v in values
    ]
    null_wire = zigpy_type(null_val).serialize()

    return assemble(
        fmt_roundtrip_scalar(roundtrip, rust_type),
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_sint(zigpy_type, rust_type: str, bits: int) -> str:
    null_val = -(1 << (bits - 1))   # e.g. -128 for i8
    max_val = (1 << (bits - 1)) - 1  # e.g. 127

    values = [0, 1, -1, max_val, null_val + 1]  # null+1 = one above sentinel
    roundtrip = [
        (zigpy_type(v).serialize(), str(v))
        for v in values
    ]
    null_wire = zigpy_type(null_val).serialize()

    return assemble(
        fmt_roundtrip_scalar(roundtrip, rust_type),
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_bool() -> str:
    # ZCL bool: 0x00 = false, 0x01 = true, 0xFF = null sentinel.
    roundtrip = [
        (t.Bool(False).serialize(), "false"),
        (t.Bool(True).serialize(), "true"),
    ]
    # Values that must return ZclError::InvalidValue, not NullSentinel.
    invalid = [bytes([0x02]), bytes([0xFE])]
    null_wire = bytes([0xFF])

    return assemble(
        fmt_roundtrip_bool(roundtrip),
        fmt_bytes_list("NULL_WIRE", [null_wire]),
        "/// These must return Err(ZclError::InvalidValue), not NullSentinel.\n"
        + fmt_bytes_list("INVALID_VALUE", invalid),
    )


def gen_f32() -> str:
    # Roundtrip stored as raw bit patterns (u32) to avoid float equality issues.
    vals: list[tuple[float, str]] = [
        (0.0, "0"),
        (1.0, str(struct.unpack('<I', struct.pack('<f', 1.0))[0])),
        (-1.0, str(struct.unpack('<I', struct.pack('<f', -1.0))[0])),
        (1.5, str(struct.unpack('<I', struct.pack('<f', 1.5))[0])),
    ]
    roundtrip_raw = [
        (t.Single(v).serialize(), bits)
        for v, bits in vals
    ]
    # ZCL Single Precision null = any NaN; the canonical zigpy NaN
    null_wire = t.Single(float("nan")).serialize()

    lines = [f"    ({fmt_bytes(wire)}, {bits}u32)," for wire, bits in roundtrip_raw]
    roundtrip_section = (
        "/// Compare decoded f32 bits via f32::to_bits() to avoid NaN != NaN.\n"
        "pub static ROUNDTRIP_BITS: &[(&[u8], u32)] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )
    return assemble(
        roundtrip_section,
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_f64() -> str:
    vals: list[tuple[float, str]] = [
        (0.0, "0"),
        (1.0, str(struct.unpack('<Q', struct.pack('<d', 1.0))[0])),
        (-1.0, str(struct.unpack('<Q', struct.pack('<d', -1.0))[0])),
        (1.5, str(struct.unpack('<Q', struct.pack('<d', 1.5))[0])),
    ]
    roundtrip_raw = [
        (t.Double(v).serialize(), bits)
        for v, bits in vals
    ]
    null_wire = t.Double(float("nan")).serialize()

    lines = [f"    ({fmt_bytes(wire)}, {bits}u64)," for wire, bits in roundtrip_raw]
    roundtrip_section = (
        "/// Compare decoded f64 bits via f64::to_bits() to avoid NaN != NaN.\n"
        "pub static ROUNDTRIP_BITS: &[(&[u8], u64)] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )
    return assemble(
        roundtrip_section,
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_bitmap(zigpy_type, rust_type: str, inner: str, bits: int) -> str:
    all_ones = (1 << bits) - 1
    values = [0, 1, 0b10101010, all_ones]
    roundtrip = [
        (zigpy_type(v).serialize(), str(v))
        for v in values
        if v <= all_ones
    ]
    # Bitmaps have no null sentinel; ALL_PATTERNS_VALID = true.
    return assemble(fmt_roundtrip_bits(roundtrip, rust_type, inner))


def gen_enum8() -> str:
    # Enum decode is user-driven (the Rust callback decides valid discriminants).
    # Vectors here test that the schema passes the raw byte through correctly;
    # application-level validation is tested in cluster-specific tests.
    roundtrip = [
        (t.enum8(v).serialize(), str(v))
        for v in [0x00, 0x01, 0x42, 0xFE]
    ]
    null_wire = t.enum8(0xFF).serialize()
    return assemble(
        fmt_roundtrip_scalar(roundtrip, "u8"),
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_enum16() -> str:
    roundtrip = [
        (t.enum16(v).serialize(), str(v))
        for v in [0x0000, 0x0001, 0x0100, 0xFFFE]
    ]
    null_wire = t.enum16(0xFFFF).serialize()
    return assemble(
        fmt_roundtrip_scalar(roundtrip, "u16"),
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_short_text() -> str:
    # Wire: [len: u8, utf8 bytes...]  null sentinel: len = 0xFF
    def encode(s: str) -> bytes:
        b = s.encode("utf-8")
        return bytes([len(b)]) + b

    roundtrip_raw = [
        ("empty",   encode(""),        '""'),
        ("ascii",   encode("hello"),   '"hello"'),
        ("unicode", encode("café"),    '"café"'),
    ]
    null_wire = bytes([0xFF])
    invalid_utf8 = bytes([0x03, 0xFF, 0xFE, 0xFD])  # length=3, invalid UTF-8

    lines = [f"    ({fmt_bytes(wire)}, {s})," for _, wire, s in roundtrip_raw]
    rt = "pub static ROUNDTRIP: &[(&[u8], &str)] = &[\n" + "\n".join(lines) + "\n];\n"

    return assemble(
        rt,
        fmt_bytes_list("NULL_WIRE", [null_wire]),
        "/// Must return Err(ZclError::InvalidUtf8).\n"
        + fmt_bytes_list("INVALID_UTF8", [invalid_utf8]),
    )


def gen_short_octet_string() -> str:
    def encode(b: bytes) -> bytes:
        return bytes([len(b)]) + b

    roundtrip_raw = [
        (encode(b""),        "b\"\""),
        (encode(b"\x00"),    "b\"\\x00\""),
        (encode(b"\xDE\xAD\xBE\xEF"), "b\"\\xDE\\xAD\\xBE\\xEF\""),
    ]
    null_wire = bytes([0xFF])

    lines = [f"    ({fmt_bytes(wire)}, {s})," for wire, s in roundtrip_raw]
    rt = "pub static ROUNDTRIP: &[(&[u8], &[u8])] = &[\n" + "\n".join(lines) + "\n];\n"

    return assemble(
        rt,
        fmt_bytes_list("NULL_WIRE", [null_wire]),
    )


def gen_array_u16() -> str:
    """
    Wire format: [element_type: u8 (0x21), count: u16 LE, elements...]
    Null sentinel: count = 0xFFFF.
    """
    def encode_array(element_type: int, elements: list[int], elem_size: int) -> bytes:
        count = len(elements)
        payload = b"".join(struct.pack("<H", e) for e in elements)
        return bytes([element_type]) + struct.pack("<H", count) + payload

    empty    = encode_array(0x21, [], 2)
    one_elem = encode_array(0x21, [1], 2)
    multi    = encode_array(0x21, [1, 2, 3], 2)
    null_wire = bytes([0x21, 0xFF, 0xFF])  # element_type=u16, count=null sentinel

    return assemble(
        "/// (wire, expected element count)\n"
        "pub static ROUNDTRIP_COUNTS: &[(&[u8], usize)] = &[\n"
        f"    ({fmt_bytes(empty)}, 0),\n"
        f"    ({fmt_bytes(one_elem)}, 1),\n"
        f"    ({fmt_bytes(multi)}, 3),\n"
        "];\n",
        fmt_bytes_list("NULL_WIRE", [null_wire]),
        "/// Must return Err(ZclError::TypeIdMismatch) — element_type byte is wrong.\n"
        + fmt_bytes_list(
            "TYPE_MISMATCH",
            [bytes([0x20, 0x01, 0x00, 0x00, 0x00])]  # says u8 but encoded as u16
        ),
    )


def gen_set_u8() -> str:
    """Set requires unique elements; duplicates must return Err(ZclError::InvalidValue)."""
    def encode_set(elements: list[int]) -> bytes:
        count = len(elements)
        payload = bytes(elements)
        return bytes([0x20]) + struct.pack("<H", count) + payload

    unique_two  = encode_set([0x01, 0x02])
    unique_three = encode_set([0x0A, 0x0B, 0x0C])
    dup         = encode_set([0x01, 0x01])  # duplicate → must fail

    return assemble(
        "pub static VALID: &[&[u8]] = &[\n"
        f"    {fmt_bytes(unique_two)},\n"
        f"    {fmt_bytes(unique_three)},\n"
        "];\n",
        "/// Must return Err(ZclError::InvalidValue) — duplicate elements.\n"
        + fmt_bytes_list("DUPLICATE_ELEMENT", [dup]),
    )


