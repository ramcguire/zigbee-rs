"""
ZCL test vector generator.

Uses zigpy as the reference implementation to produce byte-exact
encodings for each ZCL type supported by zigbee-cluster-library.

Run via xtask (preferred):
    cargo xtask generate-vectors   # write to zigbee-cluster-library/tests/vectors/
    cargo xtask check-vectors      # diff against committed vectors; exit 1 on drift

Or directly:
    uv run scripts/gen_vectors.py --out zigbee-cluster-library/tests/vectors/
    uv run scripts/gen_vectors.py --out /tmp/check --check
"""

