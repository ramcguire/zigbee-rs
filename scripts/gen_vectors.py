def _encode_coll(elem_type_id: int, payload: bytes, count: int) -> bytes:
    """Wire: [elem_type_id, count_lo, count_hi, payload...]"""
    return bytes([elem_type_id]) + struct.pack("<H", count) + payload


def _encode_coll_null(elem_type_id: int) -> bytes:
    return bytes([elem_type_id, 0xFF, 0xFF])


def fmt_roundtrip_collection(cases: list[tuple[bytes, list]], rust_elem_type: str, rust_suffix: str) -> str:
    def fmt_elems(elems: list) -> str:
        if not elems:
            return "&[]"
        if rust_elem_type == "bool":
            parts = ["true" if e else "false" for e in elems]
        else:
            parts = [f"{e}{rust_suffix}" for e in elems]
        return "&[" + ", ".join(parts) + "]"

    lines = [f"    ({fmt_bytes(wire)}, {fmt_elems(elems)})," for wire, elems in cases]
    return (
        f"pub static ROUNDTRIP: &[(&[u8], &[{rust_elem_type}])] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )


def gen_collection(
    elem_type_id: int,
    elem_size: int,
    serialize_fn,
    rust_elem_type: str,
    rust_suffix: str,
    sample_sets: list[list],
    wrong_elem_type_id: int,
) -> str:
    """
    Generate collection vectors: ROUNDTRIP + NULL_WIRE + TYPE_MISMATCH.
    Works for both Array and Bag (same wire format — only the schema kind differs).
    """
    roundtrip_cases = []
    for elems in sample_sets:
        payload = b"".join(serialize_fn(e) for e in elems)
        wire = _encode_coll(elem_type_id, payload, len(elems))
        roundtrip_cases.append((wire, elems))

    null_wire = _encode_coll_null(elem_type_id)
    # type mismatch: wrong element_type_id byte, one element of `elem_size` zeros
    mismatch_wire = _encode_coll(wrong_elem_type_id, bytes(elem_size), 1)

    return assemble(
        fmt_roundtrip_collection(roundtrip_cases, rust_elem_type, rust_suffix),
        fmt_bytes_list("NULL_WIRE", [null_wire]),
        "/// Must return Err(ZclError::TypeIdMismatch) — element_type byte is wrong.\n"
        + fmt_bytes_list("TYPE_MISMATCH", [mismatch_wire]),
    )


def gen_collection_set(
    elem_type_id: int,
    serialize_fn,
    rust_elem_type: str,
    rust_suffix: str,
    unique_sample_sets: list[list],
    duplicate_sample: list,
) -> str:
    """Generate set vectors: VALID (with element values) + DUPLICATE."""
    def fmt_elems(elems: list) -> str:
        if not elems:
            return "&[]"
        if rust_elem_type == "bool":
            parts = ["true" if e else "false" for e in elems]
        else:
            parts = [f"{e}{rust_suffix}" for e in elems]
        return "&[" + ", ".join(parts) + "]"

    valid_cases = []
    for elems in unique_sample_sets:
        payload = b"".join(serialize_fn(e) for e in elems)
        wire = _encode_coll(elem_type_id, payload, len(elems))
        valid_cases.append((wire, elems))

    dup_payload = b"".join(serialize_fn(e) for e in duplicate_sample)
    dup_wire = _encode_coll(elem_type_id, dup_payload, len(duplicate_sample))

    lines = [f"    ({fmt_bytes(wire)}, {fmt_elems(elems)})," for wire, elems in valid_cases]
    valid_static = (
        f"pub static VALID: &[(&[u8], &[{rust_elem_type}])] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )

    return assemble(
        valid_static,
        "/// Must return Err(ZclError::InvalidValue) — duplicate elements.\n"
        + fmt_bytes_list("DUPLICATE", [dup_wire]),
    )


def gen_collection_struct_pair() -> str:
    """
    Typed struct with two fields: u8 + u16.
    Wire: [field_count=2 LE, 0x20 (Uint8 type), u8_val, 0x21 (Uint16 type), u16_val LE]
    Null sentinel: field_count = 0xFFFF → [0xFF, 0xFF]
    """
    def encode_pair(u8_val: int, u16_val: int) -> bytes:
        return struct.pack("<H", 2) + bytes([0x20, u8_val, 0x21]) + struct.pack("<H", u16_val)

    cases = [
        (0x42, 0x1234),
        (0x00, 0x0000),
        (0xFE, 0xFFFE),  # max non-sentinel values for u8 and u16
    ]
    lines = [
        f"    ({fmt_bytes(encode_pair(a, b))}, ({a}u8, {b}u16)),"
        for a, b in cases
    ]
    rt = (
        "pub static ROUNDTRIP: &[(&[u8], (u8, u16))] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )
    null_wire = struct.pack("<H", 0xFFFF)  # field_count = null sentinel
    return assemble(rt, fmt_bytes_list("NULL_WIRE", [null_wire]))


def gen_nested_array_u8() -> str:
    """
    ArrayOf<ArrayOf<u8>>: outer array where each element is itself an ArrayOf<u8>.
    Outer wire: [0x48 (Array elem_type), count_lo, count_hi, inner_arrays...]
    Each inner array: [0x20 (u8 elem_type), count_lo, count_hi, u8_bytes...]
    Null sentinel: outer count = 0xFFFF → [0x48, 0xFF, 0xFF]
    """
    ARRAY_TYPE_ID = 0x48
    U8_TYPE_ID = 0x20

    def encode_inner(elems: list) -> bytes:
        return _encode_coll(U8_TYPE_ID, bytes(elems), len(elems))

    sample_sets = [[], [[]], [[1, 2], [3]]]

    lines = []
    for inner_lists in sample_sets:
        outer_payload = b"".join(encode_inner(lst) for lst in inner_lists)
        outer_wire = _encode_coll(ARRAY_TYPE_ID, outer_payload, len(inner_lists))
        inner_wire_strs = [fmt_bytes(encode_inner(lst)) for lst in inner_lists]
        inner_static = "&[" + ", ".join(inner_wire_strs) + "]" if inner_wire_strs else "&[]"
        lines.append(f"    ({fmt_bytes(outer_wire)}, {inner_static}),")

    rt = (
        "/// (outer_wire, inner_element_wires) — each inner wire is a valid ArrayOf<u8> encoding.\n"
        "pub static ROUNDTRIP: &[(&[u8], &[&[u8]])] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )
    null_wire = _encode_coll_null(ARRAY_TYPE_ID)
    mismatch_wire = _encode_coll(U8_TYPE_ID, bytes(1), 1)
    return assemble(
        rt,
        fmt_bytes_list("NULL_WIRE", [null_wire]),
        "/// Must return Err(ZclError::TypeIdMismatch) — element_type byte is wrong.\n"
        + fmt_bytes_list("TYPE_MISMATCH", [mismatch_wire]),
    )


def gen_array_of_struct_pair() -> str:
    """
    ArrayOf<StructOf<PairStruct>>: array where each element is a (u8, u16) struct.
    Outer wire: [0x4C (Structure elem_type), count_lo, count_hi, structs...]
    Each struct: [field_count=2 LE, 0x20 (u8 type), u8_val, 0x21 (u16 type), u16_val LE]
    Null sentinel: outer count = 0xFFFF → [0x4C, 0xFF, 0xFF]
    """
    STRUCT_TYPE_ID = 0x4C

    def encode_pair_struct(u8_val: int, u16_val: int) -> bytes:
        return struct.pack("<H", 2) + bytes([0x20, u8_val, 0x21]) + struct.pack("<H", u16_val)

    sample_sets = [
        [],
        [(0x42, 0x1234)],
        [(0, 0), (0x42, 0x1234)],
    ]

    lines = []
    for pairs in sample_sets:
        payload = b"".join(encode_pair_struct(u8v, u16v) for u8v, u16v in pairs)
        outer_wire = _encode_coll(STRUCT_TYPE_ID, payload, len(pairs))
        rust_elems = "&[" + ", ".join(f"({u8v}u8, {u16v}u16)" for u8v, u16v in pairs) + "]"
        lines.append(f"    ({fmt_bytes(outer_wire)}, {rust_elems}),")

    rt = (
        "pub static ROUNDTRIP: &[(&[u8], &[(u8, u16)])] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )
    null_wire = _encode_coll_null(STRUCT_TYPE_ID)
    mismatch_wire = _encode_coll(0x20, bytes(1), 1)
    return assemble(
        rt,
        fmt_bytes_list("NULL_WIRE", [null_wire]),
        "/// Must return Err(ZclError::TypeIdMismatch) — element_type byte is wrong.\n"
        + fmt_bytes_list("TYPE_MISMATCH", [mismatch_wire]),
    )


def gen_struct_with_array() -> str:
    """
    Struct with fields (u8, ArrayOf<u8>).
    Wire: [field_count=2 LE, 0x20 (u8 type), u8_val, 0x48 (Array type), array_encoding...]
    Array encoding: [0x20 (u8 elem_type), count_lo, count_hi, u8_bytes...]
    Null sentinel: field_count = 0xFFFF → [0xFF, 0xFF]
    """
    U8_TYPE_ID = 0x20
    ARRAY_TYPE_ID = 0x48

    def encode_struct(u8_val: int, arr_elems: list) -> bytes:
        arr_enc = _encode_coll(U8_TYPE_ID, bytes(arr_elems), len(arr_elems))
        return struct.pack("<H", 2) + bytes([U8_TYPE_ID, u8_val, ARRAY_TYPE_ID]) + arr_enc

    samples = [(0, []), (42, [1, 2, 3]), (254, [10, 20])]

    lines = []
    for u8_val, arr_elems in samples:
        wire = encode_struct(u8_val, arr_elems)
        rust_arr = ", ".join(f"{e}u8" for e in arr_elems)
        lines.append(f"    ({fmt_bytes(wire)}, ({u8_val}u8, &[{rust_arr}])),")

    rt = (
        "/// (wire, (u8_field, array_field_elements))\n"
        "pub static ROUNDTRIP: &[(&[u8], (u8, &[u8]))] = &[\n"
        + "\n".join(lines)
        + "\n];\n"
    )
    null_wire = bytes([0xFF, 0xFF])
    return assemble(rt, fmt_bytes_list("NULL_WIRE", [null_wire]))


