def write_or_check(path: Path, content: str, check: bool) -> bool:
    """Return True if content matches what is on disk (or was written). False if drift."""
    if check:
        existing = path.read_text() if path.exists() else ""
        if existing == content:
            return True
        diff = difflib.unified_diff(
            existing.splitlines(keepends=True),
            content.splitlines(keepends=True),
            fromfile=str(path),
            tofile="generated",
        )
        sys.stderr.write("".join(diff))
        return False
    else:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)
        return True


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--out", required=True, type=Path, help="Output directory")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Diff against existing files; exit 1 if any differ (no writes)",
    )
    args = parser.parse_args()

    ok = True
    for name, (filename, gen_fn) in GENERATORS.items():
        try:
            content = gen_fn()
        except Exception as exc:
            print(f"  ERROR {name}: {exc}", file=sys.stderr)
            ok = False
            continue

        dest = args.out / filename
        if write_or_check(dest, content, args.check):
            if not args.check:
                print(f"  wrote {dest}")
        else:
            print(f"  DRIFT {dest}", file=sys.stderr)
            ok = False

    if not ok and args.check:
        print(
            "\nVectors out of date — run `cargo xtask generate-vectors` to refresh.",
            file=sys.stderr,
        )
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
